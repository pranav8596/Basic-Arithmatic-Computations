using System;
using System.Collections.Generic;
using System.Text;

namespace ArithmaticOperations
{
    public class ArithmaticComputation
    {
        //Compute addition of two numbers
        public int addition(int numberOne, int numberTwo)
        {
            return numberOne + numberTwo;
        }

        //Compute subtraction of two numbers
        public int subtraction(int numberOne, int numberTwo)
        {
            return numberOne - numberTwo;
        }

        //Compute multiplication of two numbers
        public int multiplication(int numberOne, int numberTwo)
        {
            return numberOne * numberTwo;
        }

        //Compute division of two numbers
        public int division(int numberOne, int numberTwo)
        {
            return numberOne / numberTwo;
        }
    }
}
